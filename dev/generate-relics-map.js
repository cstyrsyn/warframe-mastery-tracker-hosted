// generate-relics-map.js
// Reads dev/relics.json (from extract-relics.js) and writes relics.js next to data.js.
//
// Usage:
//   node dev/generate-relics-map.js           # write relics.js
//   node dev/generate-relics-map.js --dry-run  # print stats only
//
// Output: relics.js containing:
//   RELIC_INDEX  — array of all relic names (indexed for compact storage)
//   RELIC_VAULTED — Set of indices of currently-vaulted relics
//   RELIC_DROPS  — Map: itemName → [isVaulted, { partName: [[relicIdx, rarity], ...] }]
//     rarity: 0=Common  1=Uncommon  2=Rare
//     Each item's drops are sorted: non-vaulted relics first, then by rarity ascending.

'use strict';

const fs   = require('fs');
const path = require('path');

const IN_JSON = path.join(__dirname, 'relics.json');
const OUT_JS  = path.join(__dirname, '..', 'relics.js');

const RARITY_ORDER = { Common: 0, Uncommon: 1, Rare: 2 };

function main() {
  const args   = process.argv.slice(2);
  const dryRun = args.includes('--dry-run');

  if (!fs.existsSync(IN_JSON)) throw new Error('relics.json not found — run extract-relics.js first');

  const primeData = JSON.parse(fs.readFileSync(IN_JSON, 'utf-8'));
  const items = Object.keys(primeData).sort();

  // Collect all relic names and sort them
  const relicSet = new Set();
  for (const item of Object.values(primeData)) {
    for (const part of Object.values(item.Parts)) {
      for (const relic of Object.keys(part.Drops)) relicSet.add(relic);
    }
  }
  const relicIndex = [...relicSet].sort();
  const relicPos   = new Map(relicIndex.map((name, i) => [name, i]));

  // Determine which relic indices are vaulted
  const vaultedIndices = [];
  for (const [name, entry] of Object.entries(
    JSON.parse(fs.readFileSync(IN_JSON, 'utf-8')) // re-use primeData for drop info
  )) {
    for (const part of Object.values(entry.Parts)) {
      for (const [relic, info] of Object.entries(part.Drops)) {
        if (info.Vaulted) {
          const idx = relicPos.get(relic);
          if (idx !== undefined && !vaultedIndices.includes(idx)) vaultedIndices.push(idx);
        }
      }
    }
  }
  vaultedIndices.sort((a, b) => a - b);

  // Stats
  const totalDrops = items.reduce((sum, name) => {
    const p = primeData[name].Parts;
    return sum + Object.values(p).reduce((s, part) => s + Object.keys(part.Drops).length, 0);
  }, 0);
  console.log(`Items: ${items.length}  Relics: ${relicIndex.length}  Vaulted relics: ${vaultedIndices.length}  Drop entries: ${totalDrops}`);

  if (dryRun) { console.log('Dry run — no file written.'); return; }

  // ── Build RELIC_DROPS entries ────────────────────────────────────────────────
  const mapLines = [];
  for (const itemName of items) {
    const item = primeData[itemName];
    const isVaulted = item.IsVaulted ? 1 : 0;

    const partsObj = {};
    for (const [partName, partData] of Object.entries(item.Parts)) {
      const drops = Object.entries(partData.Drops)
        .map(([relic, info]) => [relicPos.get(relic), RARITY_ORDER[info.Rarity] ?? 0, info.Vaulted ? 1 : 0])
        .sort((a, b) => {
          // Non-vaulted first, then by rarity ascending (Common → Rare)
          if (a[2] !== b[2]) return a[2] - b[2];
          return a[1] - b[1];
        })
        .map(([idx, rarity, v]) => v ? [idx, rarity, 1] : [idx, rarity]);
      partsObj[partName] = drops;
    }

    mapLines.push(`[${JSON.stringify(itemName)},[${isVaulted},${JSON.stringify(partsObj)}]]`);
  }

  // ── Assemble relics.js ───────────────────────────────────────────────────────
  const lines = [
    '// Auto-generated by dev/generate-relics-map.js — do not edit manually.',
    '// Update workflow: node dev/extract-relics.js && node dev/generate-relics-map.js',
    '//',
    '// RELIC_INDEX[i]  = relic name',
    '// RELIC_VAULTED   = Set of indices of currently-vaulted relics',
    '// RELIC_DROPS     = Map: itemName → [isVaulted, partsObj]',
    '//   partsObj: { partName: [[relicIdx, rarity], ...] }  rarity 0=C 1=U 2=R',
    '//   Third element of each drop tuple is 1 if that specific relic is vaulted.',
    '',
    `const RELIC_INDEX=${JSON.stringify(relicIndex)};`,
    `const RELIC_VAULTED=new Set(${JSON.stringify(vaultedIndices)});`,
    `const RELIC_DROPS=new Map([\n${mapLines.map(l => `  ${l},`).join('\n')}\n]);`,
  ];

  const content = lines.join('\n') + '\n';
  fs.writeFileSync(OUT_JS, content, 'utf-8');
  console.log(`Saved → ${OUT_JS}  (${(content.length / 1024).toFixed(1)} KB)`);
  console.log('Done.');
}

main();
